    console.log("The min score is: " + min);
    console.log("The min index is: " + min_index);
    console.log("The champion is: " + data[min_index]["Champion"]);

    // Remove the children of the div
    var src = document.getElementById("result");
    while (src.firstChild) {
        src.removeChild(src.firstChild);
    }

    // Add H2 tag to the div
    var h2 = document.createElement("h2");
    h2.innerHTML = "Your Champion is: " + data[min_index]["Champion"];
