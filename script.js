    var input = document.getElementById("name_box").value;
    // Get the birthday from calender
    var birthday = document.getElementById("birthday_calender").value;
    // Split it into year, month and day
    var birthday_array = birthday.split("-");
    var birthday_month = parseInt(birthday_array[1]);
    var birthday_day = parseInt(birthday_array[2]);

    // Get the current date and time
    var current_date = new Date();
    // Get time in 24 hour format
    var current_hour = current_date.getHours();
    var current_minute = current_date.getMinutes();

    // Print in console
    console.log("The input is: " + input);
    console.log("The birthday is: " + birthday);

    // Iterate through the data
    var min = 0;
