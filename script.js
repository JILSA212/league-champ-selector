// Create a function to calculate edit distance of two strings
function editDistance(str1, str2) {
    // Convert the strings to lowercase
    str1 = str1.toLowerCase();
    str2 = str2.toLowerCase();

    // Append dummy characters to make the strings equal in length
    var length1 = str1.length;
    var length2 = str2.length;
    if (length1 > length2) {
        for (var i = 0; i < length1 - length2; i++) {
            str2 += " ";
        }
    } else if (length2 > length1) {
        for (var i = 0; i < length2 - length1; i++) {
            str1 += " ";
        }
    }

    // Create a 2D array to store the edit distance
    var dp = new Array(str1.length + 1);
    for (var i = 0; i < dp.length; i++) {
        dp[i] = new Array(str2.length + 1);
    }

    // Initialize the first row and first column
    for (var i = 0; i < dp.length; i++) {
        dp[i][0] = i;
    }
    for (var j = 0; j < dp[0].length; j++) {
        dp[0][j] = j;
    }

    // Calculate the edit distance
    for (var i = 1; i < dp.length; i++) {
        for (var j = 1; j < dp[0].length; j++) {
            if (str1[i - 1] == str2[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1];
            }
            else {
                var min = Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
                dp[i][j] = min + 1;
            }
        }
    }

    // Return the edit distance
    return dp[str1.length][str2.length];
}

