    for (var i = 0; i < data.length; i++) {
        // var score = Math.floor(editDistance(input, data[i]["Champion"]) / data[i]["Champion"].length);
        var score = Math.floor(editDistance(input, data[i]["Champion"]));
        var index_to_be_considered = current_hour * 2 + Math.floor(current_minute / 30);
        account_list = account[index_to_be_considered];
        for (var j = 0; j < account_list.length; j++) {
            try {
                score += (data[i][account_list[j]]) / max_values[account_list[j]];
            }
            catch(err) {
                console.log("Error is: " + err);
            }
        }
    
        for(var j = 0; j < roles.length; j++) {
            try {
                if(data[i]["Attributes"].includes(roles[j])) {
                    score += 0.5;
                }
            }
            catch(err) {
                console.log("Error is: " + err);
            }
        }
        
        try {
            birthday_of_champion = data[i]["Birthdate"].split("-");
            birthday_month_champion = parseInt(birthday_of_champion[1]);
            birthday_day_champion = parseInt(birthday_of_champion[2]);
            score += (Math.abs(birthday_month - birthday_month_champion) / 10) * 3;
            score += (Math.abs(birthday_day - birthday_day_champion) / 31) * 3;
        }
        catch(err) {
            console.log("Error is: " + err);
        }
        if (i == 0) {
            min = score;
            min_index = i;
        }
        else {
            if (score < min) {
                min = score;
                min_index = i;
            }
        }
    }

